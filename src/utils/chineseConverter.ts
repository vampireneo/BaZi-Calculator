/**
 * 簡體中文轉繁體中文轉換器
 * 用於轉換農曆日期中的簡體字為繁體字
 */

// 常見的簡繁轉換對照表
const simplifiedToTraditionalMap: Record<string, string> = {
  // 農曆相關
  '农': '農',
  '历': '曆',
  '闰': '閏',
  '腊': '臘',
  // 月份
  '正': '正',
  '冬': '冬',
  // 數字
  '一': '一',
  '二': '二',
  '三': '三',
  '四': '四',
  '五': '五',
  '六': '六',
  '七': '七',
  '八': '八',
  '九': '九',
  '十': '十',
  // 日期
  '初': '初',
  '廿': '廿',
  '卅': '卅',
  // 年份天干地支
  '甲': '甲',
  '乙': '乙',
  '丙': '丙',
  '丁': '丁',
  '戊': '戊',
  '己': '己',
  '庚': '庚',
  '辛': '辛',
  '壬': '壬',
  '癸': '癸',
  '子': '子',
  '丑': '丑',
  '寅': '寅',
  '卯': '卯',
  '辰': '辰',
  '巳': '巳',
  '午': '午',
  '未': '未',
  '申': '申',
  '酉': '酉',
  '戌': '戌',
  '亥': '亥',
  // 其他常見簡繁轉換
  '年': '年',
  '月': '月',
  '日': '日',
};

/**
 * 將簡體中文轉換為繁體中文
 * @param text 簡體中文文本
 * @returns 繁體中文文本
 */
export function convertToTraditional(text: string): string {
  return text
    .split('')
    .map(char => simplifiedToTraditionalMap[char] || char)
    .join('');
}
